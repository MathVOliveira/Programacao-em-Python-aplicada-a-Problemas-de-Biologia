# BOOLEANOS

# Operadores Lógicos
print("LÓGICOS")
a = True
b = False
print(a, b)
print(a and b) # retorna True se as duas var for True, se não, retorna False
print(a or b) # retorna True se alguma das duas for True
print(a | b) # mesma coisa do OR
print(not a, not b) # basicamente é uma negação, mostrando o 'contrário'

print() #pulando linha

# Operadores Relacionais (fazem a relação entre variáveis)
print("RELACIONAIS")
print(5 > 3) # maior que
print(5 < 3) # menor que
print(5 >= 3) # maior ou igual
print(5 <= 3) # menor ou igual
print(5 == 3) # igual à (lembrando que comparação é com 2 '==', com apenas 1 é atribuição)
print(5 != 3) # diferente de