# Variáveis inteiras - int
numero = 3
print(numero)
print(type(numero))

num = int(input("Digite um número: "))
print(num)
print(type(num))

# Variáveis ponto Flutuante - FLOAT
pi = 3.14
print(pi)
print(type(pi))

num2 = float(input("Digite um número: "))
print(num2)
print(type(num2))

# Variáveis strings e chars - pode ser uma única letra ou frases inteiras
letra = 'a'
palavra1 = "linguagem de programação"
print(letra, palavra1)
print(type(letra), type(palavra1))
# para str não é necessário definir o tipo de var, mas pode-se usar str no começo
palavra2 = input("Digite uma letra/palavra/frase: ") 
print(palavra2)
print(type(palavra2))